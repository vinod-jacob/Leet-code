#!/bin/bash

t()
{
    command=$(./divide2Ints $1 $2)
    if [ $command == "$3" ]; then
        echo -e "\e[32m PASS \e[36m $1 $2\e[0m"
    else 
        echo -e "\e[1m \e[31m FAIL --> input= $1 $2 --- expected output= $3 --- actual output= $command \e[0m"
    fi
}

